// areaperi.h

// Macro to calculate the area of a triangle (base * height / 2)
#define AREA_TRIANGLE(base, height) ((base) * (height) / 2)

// Macro to calculate the perimeter of a triangle (a + b + c)
#define PERIMETER_TRIANGLE(a, b, c) ((a) + (b) + (c))

// Macro to calculate the area of a square (side * side)
#define AREA_SQUARE(side) ((side) * (side))

// Macro to calculate the perimeter of a square (4 * side)
#define PERIMETER_SQUARE(side) (4 * (side))

// Macro to calculate the area of a circle (PI * radius * radius)
#define AREA_CIRCLE(radius) (3.14159 * (radius) * (radius))

// Macro to calculate the perimeter of a circle (2 * PI * radius)
#define PERIMETER_CIRCLE(radius) (2 * 3.14159 * (radius))